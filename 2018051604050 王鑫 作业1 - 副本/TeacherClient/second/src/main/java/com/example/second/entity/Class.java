package com.example.second.entity;

public class Class {
    String classId;//课程id
    String className;//课程名
    String classTeacher;//任课教师
    String classTarget;//课程目标
    String classTimeBegin;//开课时间
    String classTimes;//学时

    public String getClassId() {
        return classId;
    }

    public void setClassId(String classId) {
        this.classId = classId;
    }

    public String getClassName() {
        return className;
    }

    public void setClassName(String className) {
        this.className = className;
    }

    public String getClassTeacher() {
        return classTeacher;
    }

    public void setClassTeacher(String classTeacher) {
        this.classTeacher = classTeacher;
    }

    public String getClassTarget() {
        return classTarget;
    }

    public void setClassTarget(String classTarget) {
        this.classTarget = classTarget;
    }

    public String getClassTimeBegin() {
        return classTimeBegin;
    }

    public void setClassTimeBegin(String classTimeBegin) {
        this.classTimeBegin = classTimeBegin;
    }

    public String getClassTimes() {
        return classTimes;
    }

    public void setClassTimes(String classTimes) {
        this.classTimes = classTimes;
    }
}
